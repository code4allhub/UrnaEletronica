    private void btnConfirmaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConfirmaActionPerformed
        String numeroP = lblNum1.getText() + lblNum2.getText();
        if(!lblNum1.getText().equals("") & !lblNum2.getText().equals("")){
            for (Candidato candidato : candidatos) {
                if (candidato.numero.equals(numeroP)) {
                    candidato.votos = candidato.votos +1;
                    break;
                } 
            }
            if (lblVotoBranco.getText().equals("VOTO EM BRANCO")){
            votosBrancos = votosBrancos +1;
           } else if(lblVotoNulo.isVisible()& lblVotoNulo.getText().equals("VOTO NULO")&lblNumeroErrado.getText().equals("NÚMERO ERRADO")){
            votosNulos = votosNulos +1;
            }
            esconderFotos();
            lblNum1.setText("");
            lblNum2.setText("");
            lblPrefeito.setText("PREFEITO(A)");
            panelNum1.setVisible(true);
            panelNum2.setVisible(true);
            panelFim.setVisible(true);
            
            
            t=new Timer(3000, new ActionListener(){
            @Override
                public void actionPerformed(ActionEvent e) {
                    panelFim.setVisible(false);
                    System.out.println("Tela fim desaparece.");
                }
            });
            t.start();
        }
        t.setRepeats(false);
    }//GEN-LAST:event_btnConfirmaActionPerformed

