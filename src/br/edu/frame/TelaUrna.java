    public void telaFinal() {
        for (Candidato candidato : candidatos) {
            somaVotosValidos =somaVotosValidos+ candidato.votos;
        }
        if(senha.equals(senhaurna)&somaVotosValidos>0){
            this.dispose();
            new TelaFinal(candidatos, votosBrancos, votosNulos).setVisible(true);
            
//             TelaUrna.setVisible(false);
        } else if (senha.equals(senhaurna)&somaVotosValidos<1) {
            JOptionPane.showMessageDialog(
        null, "Sem votos válidos.", "Erro", JOptionPane.ERROR_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(
        null, "Senha incorreta.", "Erro", JOptionPane.ERROR_MESSAGE);
        }
    }
    

